#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	int		i;
	int		l;

	i = 0;
	l = 0;
	while (src[i] != '\0')
	{
		dst[l] = src[i];
		i++;
		l++;
	}
	dst[l] = '\0';
	return (dst);
}
