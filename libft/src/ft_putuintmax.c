#include "libft.h"

void	ft_putuintmax(uintmax_t nb)
{
	if (nb >= 10)
	{
		ft_putuintmax(nb / 10);
		ft_putuintmax(nb % 10);
	}
	else
		ft_putchar(nb + '0');
}
