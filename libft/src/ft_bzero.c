#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*str;
	size_t	i;

	i = 0;
	str = s;
	if (!n)
		return ;
	while (i < n)
	{
		str[i] = 0;
		i++;
	}
}
